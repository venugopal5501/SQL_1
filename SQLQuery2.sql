CREATE TABLE PRODUCT(ID INT PRIMARY KEY, NAME VARCHAR(30),DESCRIPTION VARCHAR(30), SIZE VARCHAR(18), COLOR VARCHAR(8), QUANTITY INT, UNIT_PRICE NUMERIC(15,2));

CREATE TABLE CUSTOMERS(ID INT PRIMARY KEY, FNAME VARCHAR(15), LNAME VARCHAR(20),ADDRESS VARCHAR(35),CITY VARCHAR(20),STATE VARCHAR(20),ZIP VARCHAR(10),PHONE VARCHAR(12), COMAPANY_NAME VARCHAR(35) );

CREATE TABLE CONTACT (ID INT PRIMARY KEY, LAST_NAME VARCHAR(15), FNAME VARCHAR(15), TITLE VARCHAR(2), STREET VARCHAR(30), CITY VARCHAR(20), STATE VARCHAR(2), ZIP VARCHAR(5), PHONE VARCHAR(10), FAX VARCHAR(10));

CREATE TABLE FIN_CODE (CODE VARCHAR(2) PRIMARY KEY, TYPE VARCHAR(10), DESCRIPTION VARCHAR(50));

CREATE TABLE SALE_ORDER_ITEMS(ID INT, LINE_ID SMALLINT, PROD_ID INT CONSTRAINT FK_QWE FOREIGN KEY  REFERENCES PRODUCT(ID), QUANTITY INT, SHIP_DATE DATE, PRIMARY KEY(ID,LINE_ID) );
