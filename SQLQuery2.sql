CREATE TABLE SALES_ORDER(ID INT PRIMARY KEY, CUST_ID INT CONSTRAINT FK_ORDERS FOREIGN KEY REFERENCES CUSTOMERS(ID),ORDERDATE DATE, FIN_CODE_ID VARCHAR(2) CONSTRAINT FK_CODE FOREIGN KEY REFERENCES FIN_CODE(CODE), REGION VARCHAR(7), SALES_REP INT CONSTRAINT FK_EMPLOYEE FOREIGN KEY REFERENCES EMPLOYEE(EMP_ID));
